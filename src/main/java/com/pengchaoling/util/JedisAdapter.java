    /**
     *  执行事务队列里面的命令
     */
    public List<Object> exec(Transaction tx, Jedis jedis) {
        try {
            return tx.exec();
        } catch (Exception e) {
            logger.error("发生异常" + e.getMessage());
            tx.discard();
        } finally {
            //如果出现问题就把整个都关掉，命令队列里面的命令都不会执行了，这个事务跟mysql还是不一样哦 这里不需要回滚 因为本来就还没执行
            if (tx != null) {
                try {
                    tx.close();
                } catch (IOException ioe) {
                    // ..
                }
            }

            if (jedis != null) {
                jedis.close();
            }
        }
        return null;
    }

